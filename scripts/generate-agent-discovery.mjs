import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const packageJson = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));
const tauriConfig = JSON.parse(readFileSync(join(root, "src-tauri", "tauri.conf.json"), "utf8"));

const productName = tauriConfig.productName ?? "PLVS";
const identifier = tauriConfig.identifier ?? "com.soundoer.plvs";
const version = packageJson.version;

const manifest = {
  schemaVersion: 2,
  productName,
  identifier,
  version,
  cli: {
    // Path of the CLI relative to the install root, per platform. The same
    // manifest is bundled on every platform, so a single string would lie on
    // all but one of them (it used to claim the macOS path on Windows).
    relativePath: {
      windows: "plvs-cli.exe",
      macos: "Contents/MacOS/plvs-cli",
    },
    doctor: ["doctor", "--json"],
  },
};

writeFileSync(
  join(root, "src-tauri", "plvs-agent.json"),
  `${JSON.stringify(manifest, null, 2)}\n`
);

const nsisDefines = `; Generated by scripts/generate-agent-discovery.mjs. Do not edit by hand.
!define PLVS_AGENT_PRODUCT_NAME "${productName}"
!define PLVS_AGENT_IDENTIFIER "${identifier}"
!define PLVS_AGENT_VERSION "${version}"
!define PLVS_AGENT_REG_KEY "Software\\SounDoer\\PLVS"
`;

writeFileSync(join(root, "src-tauri", "nsis", "agent-discovery.nsh"), nsisDefines);

const hookPath = join(root, "src-tauri", "nsis", "installer-hooks.nsh");
const hook = readFileSync(hookPath, "utf8");
const beginMarker = "; BEGIN GENERATED PLVS AGENT DISCOVERY";
const endMarker = "; END GENERATED PLVS AGENT DISCOVERY";
const generatedBlock = `${beginMarker}\n${nsisDefines.trimEnd()}\n${endMarker}`;
const markerPattern = new RegExp(`${beginMarker}[\\s\\S]*?${endMarker}`);
if (!markerPattern.test(hook)) {
  throw new Error("installer-hooks.nsh is missing the PLVS agent discovery marker block");
}
writeFileSync(hookPath, hook.replace(markerPattern, generatedBlock));

console.log("Wrote src-tauri/plvs-agent.json");
console.log("Wrote src-tauri/nsis/agent-discovery.nsh");
console.log("Updated src-tauri/nsis/installer-hooks.nsh");
